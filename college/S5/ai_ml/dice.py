import random

faces = {
    1: ["[-----]", "[     ]", "[  0  ]", "[     ]", "[-----]"],
    2: ["[-----]", "[0    ]", "[     ]", "[    0]", "[-----]"],
    3: ["[-----]", "[0    ]", "[  0  ]", "[    0]", "[-----]"],
    4: ["[-----]", "[0   0]", "[     ]", "[0   0]", "[-----]"],
    5: ["[-----]", "[0   0]", "[  0  ]", "[0   0]", "[-----]"],
    6: ["[-----]", "[0 0 0]", "[     ]", "[0 0 0]", "[-----]"]
}

x = "y"
while x.lower() == "y":
    d1, d2 = random.randint(1,6), random.randint(1,6)
    for l1, l2 in zip(faces[d1], faces[d2]):
        print(l1 + "   " + l2)
    x = input("Roll again? (y/n): ")
    print()

